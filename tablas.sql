CREATE TABLE BIBLIOTECA.AUTOR (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(255)  NULL,
    NACIONALIDAD VARCHAR2(255)  NULL,
	FECHA_NACIMIENTO DATE  NULL,
    ESTADO VARCHAR2(10)  NULL

);

CREATE TABLE BIBLIOTECA.LIBRO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TITULO VARCHAR2(255) NULL,
    AUTOR NUMBER NOT NULL,
    ISBN VARCHAR2(20) NULL,
    FECHA_PUBLICACION DATE NULL,
    ESTADO NUMBER NULL,
    FOREIGN KEY (AUTOR) REFERENCES BIBLIOTECA.AUTOR(ID)
);


CREATE TABLE BIBLIOTECA.PRESTAMO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    LIBRO NUMBER NOT NULL,
    FECHA_PRESTAMO DATE  NULL,
    FECHA_DEVOLUCION DATE  NULL,
    ESTADO NUMBER  NULL,
    FOREIGN KEY (LIBRO) REFERENCES BIBLIOTECA.LIBRO(ID) 
);


CREATE TABLE BIBLIOTECA.USUARIO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USUARIO VARCHAR2(255)  NULL,
    CONTRASENIA VARCHAR2(255)  NULL,
    ESTADO VARCHAR2(10)  NULL,
	FECHA_CREACION DATE NULL
	
);